public class ConstantAndDefaultDemo {
    // Constant value
    public static final int CONSTANT_VALUE = 23;
    
    // Default variables
    public static int defaultInt;
    public static double defaultDouble;
    public static boolean defaultBoolean;
    public static String defaultString;
    
    public static void main(String[] args) {
        // Printing constant value
        System.out.println("Constant value: " + CONSTANT_VALUE);
        
        // Printing default variables
        System.out.println("Default int: " + defaultInt);
        System.out.println("Default double: " + defaultDouble);
        System.out.println("Default boolean: " + defaultBoolean);
        System.out.println("Default String: " + defaultString);
    }
}
